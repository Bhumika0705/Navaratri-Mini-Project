let currentDay = 1;

// Elements
const dailyCard = document.getElementById("dailyCard");
const deviPhoto = document.getElementById("deviPhoto");
const dayTitle = document.getElementById("dayTitle");
const dayDesc = document.getElementById("dayDesc");
const outfit = document.getElementById("outfit");
const decoration = document.getElementById("decoration");
const prasadi = document.getElementById("prasadi");
const openAarti = document.getElementById("openAarti");
const modal = document.getElementById("aartiModal");
const closeModal = document.getElementById("closeModal");
const modalTitle = document.getElementById("modalTitle");
const aartiText = document.getElementById("aartiText");
const prevBtn = document.getElementById("prevBtn");
const nextBtn = document.getElementById("nextBtn");

// Render Day
function renderDay(num){
  const d = navratriDays.find(x => x.day === num);
  if(!d) return;

  dailyCard.style.background = d.color;
  deviPhoto.src = `day${d.day}.jpg`;
  dayTitle.textContent = `Day ${d.day}: ${d.title}`;
  dayDesc.textContent = d.desc;
  outfit.textContent = d.outfit;
  decoration.textContent = d.decoration;
  prasadi.textContent = d.prasadi;

  modalTitle.textContent = d.title;
  aartiText.textContent = d.aarti;
}

// Init
renderDay(currentDay);

// Next/Prev
nextBtn.addEventListener("click",()=>{
  currentDay = currentDay < 9 ? currentDay+1 : 1;
  renderDay(currentDay);
});
prevBtn.addEventListener("click",()=>{
  currentDay = currentDay > 1 ? currentDay-1 : 9;
  renderDay(currentDay);
});

// Modal
openAarti.addEventListener("click",()=> modal.style.display="block");
closeModal.addEventListener("click",()=> modal.style.display="none");

//// Countdown (Navratri start)
const navratriStart = new Date("2025-09-22T00:00:00");

function updateCountdown(){
  const now = new Date();
  const diff = navratriStart - now;

  const countdownEl = document.getElementById("countdown");

  if(diff <= 0){
    countdownEl.textContent = "🌸 Happy Navratri! 🌸";
    return;
  }

  const days = Math.floor(diff / (1000 * 60 * 60 * 24));
  const hours = Math.floor((diff / (1000 * 60 * 60)) % 24);
  const mins = Math.floor((diff / (1000 * 60)) % 60);
  const secs = Math.floor((diff / 1000) % 60);

  countdownEl.textContent = `⏳ Navratri will start in: ${days}d ${hours}h ${mins}m ${secs}s`;
}

// Initial call
updateCountdown();
// Update every 1 second
setInterval(updateCountdown, 1000);
